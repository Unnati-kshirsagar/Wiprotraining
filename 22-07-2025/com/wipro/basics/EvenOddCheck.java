package com.wipro.basics;

public class EvenOddCheck {
    public static void main(String[] args) {
        int num = 17;  // You can change this value

        if (num % 2 == 0) {
            System.out.println(num + " is even.");
        } else {
            System.out.println(num + " is odd.");
        }
    }
}
